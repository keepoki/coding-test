/**
 * 1. 두 수의 합
 * https://school.programmers.co.kr/learn/courses/30/lessons/120802
 * 정수 num1과 num2가 주어질 때, num1과 num2의 합을
 * return하도록 soltuion 함수를 완성해주세요.
 */
function solution(num1, num2) {
  return num1 + num2;
}

// ------------------------------------------------------------------------

/**
 * 2. 두 수의 차
 * https://school.programmers.co.kr/learn/courses/30/lessons/120803
 * 정수 num1과 num2가 주어질 때, num1에서 num2를 뺀 값을
 * return하도록 soltuion 함수를 완성해주세요.
 */
function solution(num1, num2) {
  return num1 - num2;
}

// ------------------------------------------------------------------------

/**
 * 3. 두 수의 곱
 * https://school.programmers.co.kr/learn/courses/30/lessons/120804
 * 정수 num1, num2가 매개변수 주어집니다. num1과 num2를 곱한 값을
 * return 하도록 solution 함수를 완성해주세요.
 */
function solution(num1, num2) {
  return num1 * num2;
}

// ------------------------------------------------------------------------

/**
 * 4. 몫 구하기
 * https://school.programmers.co.kr/learn/courses/30/lessons/120805
 * 정수 num1, num2가 매개변수로 주어질 때, num1을 num2로 나눈 몫을
 * return 하도록 solution 함수를 완성해주세요.
 */
function solution(num1, num2) {
  return parseInt(num1 / num2, 10);
}
